import 'package:flutter/material.dart';
import 'package:go_router/go_router.dart';
import 'package:injectable/injectable.dart';
import 'package:mycs/ui/planning/planning.dart';
import 'package:mycs/ui/ui_module.dart';

@singleton
class PlanningModule implements UIModule {
  final AppRouter _appRouter;

  PlanningModule(this._appRouter);

  @override
  void configure() {
    _appRouter.addRoutes(getRoutes());
  }
  @override
  List<RouteBase> getRoutes()  {
    return [
      GoRoute(
        path: '/planning',
        builder: (context, state) =>  PlanningTable(),
      )
    ];
  }

  @override
  Map<String, WidgetBuilder> getSharedWidgets() {
    // Implémentation de la méthode manquante
    return {};
  }
}